export const translations: Record<UiLocale, TranslationCatalog> = {
	en,
	"zh-CN": zhCN,
	"zh-TW": zhTW,
};

export function translateUiString(
	locale: UiLocale,
	key: TranslationKey,
	variables: TranslationVariables = {},
): string {
	const template = translations[locale][key] ?? translations.en[key];

	return template.replace(/\{(\w+)\}/g, (match, variableName: string) => {
		const value = variables[variableName];
		return value === undefined ? match : String(value);
	});
}
